class Ingrediente {
    constructor(nombre, fotoUrl, ingredientes, receta, fecha, propiedades,
        etiquetas, activo) {
        this.nombre = nombre;
        this.fotoUrl = fotoUrl;
        this.ingredientes = ingredientes;
        this.receta = receta;
        this.propiedades = propiedades;
        this.fecha = fecha;
        this.etiquetas = etiquetas;
        this.activo = activo;
    }
}